# strategy_241_slprotect.py

import logging
import json
from infra import load_indicators

log = logging.getLogger("STRATEGY_241_SLPROTECT")

class Strategy241Slprotect:
    async def validate_signal(self, signal, context):
        symbol = signal["symbol"]
        direction = signal["direction"].lower()
        tf = context["strategy"]["timeframe"].lower()

        try:
            indicators = await load_indicators(symbol, ["rsi14", "mfi14"], tf)
            rsi = indicators.get("rsi14")
            mfi = indicators.get("mfi14")

            log.debug(f"üîç [241 SLPROTECT] symbol={symbol}, direction={direction}, tf={tf}, rsi={rsi}, mfi={mfi}")

            if rsi is None or mfi is None:
                return ("ignore", "–Ω–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–π RSI/MFI")

            if direction == "long":
                if rsi < 30 and mfi < 25:
                    return True
                return ("ignore", f"—Ñ–∏–ª—å—Ç—Ä long –Ω–µ –ø—Ä–æ–π–¥–µ–Ω: rsi={rsi}, mfi={mfi}")

            elif direction == "short":
                if rsi > 70 and mfi > 75:
                    return True
                return ("ignore", f"—Ñ–∏–ª—å—Ç—Ä short –Ω–µ –ø—Ä–æ–π–¥–µ–Ω: rsi={rsi}, mfi={mfi}")

            return ("ignore", f"–Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ–µ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ: {direction}")

        except Exception as e:
            log.exception("‚ùå –û—à–∏–±–∫–∞ –≤ validate_signal")
            return ("ignore", "–æ—à–∏–±–∫–∞ –≤ —Å—Ç—Ä–∞—Ç–µ–≥–∏–∏")

    async def run(self, signal, context):
        redis = context.get("redis")
        if redis is None:
            raise RuntimeError("‚ùå Redis –∫–ª–∏–µ–Ω—Ç –Ω–µ –ø–µ—Ä–µ–¥–∞–Ω –≤ context")

        payload = {
            "strategy_id": str(signal["strategy_id"]),
            "symbol": signal["symbol"],
            "direction": signal["direction"],
            "log_uid": signal.get("log_uid"),
            "route": "new_entry",
            "received_at": signal.get("received_at")
        }

        try:
            await redis.xadd("strategy_opener_stream", {"data": json.dumps(payload)})
            log.debug(f"üì§ –°–∏–≥–Ω–∞–ª –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω: {payload}")
        except Exception as e:
            log.warning(f"‚ö†Ô∏è –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–ø—Ä–∞–≤–∫–µ —Å–∏–≥–Ω–∞–ª–∞: {e}")